        /// </summary>
        /// <param name="CardNo"></param>
        /// <returns></returns>
        public static string IdCardConvert(string CardNo)
        {
            if (string.IsNullOrEmpty(CardNo) || CardNo.Length != 15) return CardNo;

            string txtCardNo = string.Empty;
            int[] a = { 7, 9, 10, 5, 8, 4, 2, 1, 6, 3, 7, 9, 10, 5, 8, 4, 2 };
            string b = "10X98765432";

            try
            {
                int Sum = 0;
                txtCardNo = CardNo.Trim().Substring(0, 6) + "19" + CardNo.Trim().Substring(6);
                for (int i = 0; i < 17; i++)
                {
                    Sum += int.Parse(txtCardNo.Substring(i, 1).ToString()) * a[i];
                }

                int yi = Sum % 11;
                txtCardNo += b.Substring(yi, 1);
            }
            catch
            {
            }
            return txtCardNo;
        }
