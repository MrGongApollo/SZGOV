        public static string GetBirthdayFromIdcard(string CardNo)
        {
            if (string.IsNullOrEmpty(CardNo)) return "";

            if (CardNo.Length != 15 && CardNo.Length != 18) return "";

            string birth = "";
            if (CardNo.Trim().Length == 18)
            {
                birth = CardNo.Trim().Remove(14, 4);
                birth = birth.Remove(0, 6);
            }
            else
            {
                birth = CardNo.Trim().Remove(12, 3);
                birth = birth.Remove(0, 6);
                birth = "19" + birth;
            }
            birth = birth.Insert(6, "-");
            birth = birth.Insert(4, "-");
            try
            {
                DateTime.Parse(birth);
            }
            catch
            {
                birth = "";
            }
            return birth;
        }
